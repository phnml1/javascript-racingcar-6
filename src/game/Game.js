import { CARS } from "../Constants.js";
class Game {
  constructor() {}

  // 자동차 이름을 초기 전진횟수와 함께 CARS에 저장
  storeCarNames(CAR_NAMES_INPUT) {
    const CAR_NAMES = CAR_NAMES_INPUT.split(",");
    CAR_NAMES.forEach((eachCarName) => {
      CARS.push([eachCarName,0]);
    });
  }
}
export default Game;
